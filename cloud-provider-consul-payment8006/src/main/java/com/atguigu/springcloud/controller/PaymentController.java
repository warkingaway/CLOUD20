/**
 * @author czy
 * @version 1.0
 * @date 2020/5/6 22:56
 */
@RestController
@Slf4j
public class PaymentController {

    @Value("${server.port}")
    private String serverPort;

    @GetMapping("/payment/consul")
    public String p(){
        return  "springCloud with Consul: "+serverPort;
    }
}
